def readlines(filename):
    """Read all the lines in a text file and return as a list
    """
    with open(filename, 'r') as f:
        lines = f.read().splitlines()
    return lines


def draw_attention(attention_map, image=None, u=20, v=20):
    attention_map = attention_map.view(24, 80, 24, 80)
    attn_pos = attention_map[v, u, :, :]
    attn_pos = normalize_image(attn_pos)
    attn_np = attn_pos.squeeze().cpu().numpy()
    mapper = cm.ScalarMappable(cmap='rainbow')
    attn_vis = (mapper.to_rgba(attn_np)[:, :, :3] * 255).astype(np.uint8)
    result = torch.from_numpy(attn_vis).permute(2, 0, 1)
    return result


def draw_attention_stacked(attention_map, image=None, u=17, v=8):
    attention_map = attention_map.view(24, 80, 24, 80)
    output = []
    for v in range(0, 24, 10):
        for u in range(0, 80, 20):
            attn_pos = attention_map[v, u, :, :]
            attn_pos = normalize_image(attn_pos)
            attn_np = attn_pos.squeeze().cpu().numpy()
            mapper = cm.ScalarMappable(cmap='jet')
            attn_vis = (mapper.to_rgba(attn_np)[:, :, :3] * 255).astype(np.uint8)
            result = torch.from_numpy(attn_vis).permute(2, 0, 1)
            output.append(result)

    output = torch.cat(output, 1)
    return output

def draw_normals(normals):
    normals = normals.view(3, 24, 80).cpu()
    normal_map = 0.5 * normals + 0.5
    normal_map = np.uint8(normal_map.numpy() * 255)
    normal_map = normal_map[[0, 2, 1], :, :]
    normal_map = torch.from_numpy(normal_map)
    return normal_map


def normalize_image(x):
    """Rescale image pixels to span range [0, 1]
    """
    ma = float(x.max().cpu().data)
    mi = float(x.min().cpu().data)
    d = ma - mi if ma != mi else 1e5
    return (x - mi) / d


def sec_to_hm(t):
    """Convert time in seconds to time in hours, minutes and seconds
    e.g. 10239 -> (2, 50, 39)
    """
    t = int(t)
    s = t % 60
    t //= 60
    m = t % 60
    t //= 60
    return t, m, s


def sec_to_hm_str(t):
    """Convert time in seconds to a nice string
    e.g. 10239 -> '02h50m39s'
    """
    h, m, s = sec_to_hm(t)
    return "{:02d}h{:02d}m{:02d}s".format(h, m, s)


