class ResnetEncoder(nn.Module):
    """Pytorch module for a resnet encoder
    """

    def __init__(self, num_layers, pretrained, num_input_images=1):
        super(ResnetEncoder, self).__init__()

        self.num_ch_enc = np.array([64, 64, 128, 256, 512])

        resnets = {18: models.resnet18,
                   34: models.resnet34,
                   50: models.resnet50,
                   101: models.resnet101,
                   152: models.resnet152}

        if num_layers not in resnets:
            raise ValueError("{} is not a valid number of resnet layers".format(num_layers))

        if num_input_images > 1:
            self.encoder = resnet_multiimage_input(num_layers, pretrained, num_input_images)
        else:
            self.encoder = resnets[num_layers](pretrained)

        if num_layers > 34:
            self.num_ch_enc[1:] *= 4

        self.conv1 = nn.Sequential(
            nn.Conv2d(in_channels=3, out_channels=64, kernel_size=3, stride=1, dilation=1, padding=1),
            nn.BatchNorm2d(64),
            self.encoder.relu
        )

        self.conv2 = nn.Sequential(
            nn.Conv2d(in_channels=64, out_channels=64, kernel_size=3, stride=1, dilation=1, padding=1),
            nn.BatchNorm2d(64),
            self.encoder.relu
        )

        # modify the dilation in the last two resblock
        # change the downsampling stride
        for n, m in self.encoder.layer3.named_modules():
            if 'conv1' in n:
                m.stride = (1, 1)
            if 'conv2' in n:
                m.dilation, m.padding, m.stride = (2, 2), (2, 2), (1, 1)
            elif 'downsample.0' in n:
                m.stride = (1, 1)

        for n, m in self.encoder.layer4.named_modules():
            # if 'conv1' in n:
            #     m.stride = (1, 1)
            if 'conv2' in n:
                m.dilation, m.padding, m.stride = (2, 2), (2, 2), (1, 1)
            # elif 'downsample.0' in n:
            #     m.stride = (1, 1)

        # self.self_attn = Self_Attn(512, 512)

    def forward(self, input_image):
        self.features = []
        # self.attentions = {}

        x = (input_image - 0.45) / 0.225
        # Shape [B, 64, 192, 640]
        x = self.conv1(x)

        # Shape [B, 64, 192, 640]
        x = self.conv2(x)

        # Shape [B, 64, 96, 320]
        x = self.encoder.layer1(self.encoder.maxpool(x))
        self.features.append(x)

        # Shape [B, 128, 48, 160]
        x = self.encoder.layer2(x)
        self.features.append(x)

        # Shape [B, 256, 48, 160]
        x = self.encoder.layer3(x)

        # Shape [B, 512, 24, 80]
        x = self.encoder.layer4(x)

        # Dual attention
        # x, attn_context = self.self_attn(x)

        self.features.append(x)

        # self.attentions["context_attention"] = attn_context

        return self.features

